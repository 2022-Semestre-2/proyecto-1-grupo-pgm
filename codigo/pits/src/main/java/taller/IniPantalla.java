    private boolean validar(String in,String patron){
        Pattern pattern = Pattern.compile(patron, Pattern.CASE_INSENSITIVE);
        Matcher matcher = pattern.matcher(in);
        boolean matchFound = matcher.find();
        return matchFound;
    }

